function render(products, container) {
    container.innerHTML = "";

    products.slice(0, 12).forEach(item => {
        container.innerHTML += `
        <div class="card">
            <img src="${item.img}" class="card-img">
            <h2 class="title">${item.title}</h2>
            <h1 class="brand">${item.brand}</h1>
            <p class="price">${item.price}</p>
            <p class="description">this is ${item.title}</p>
        </div>`;
    });
}




let user = JSON.parse(localStorage.getItem("loggedUser")) || null;

function detectUserGender() {
    
    if (!user) {
        switchMode(heData_trend, heData_MS, "he-theme", btnHe);
        return; 
    }
    
    if (!user.gender) {
        console.log("Gender missing. Loading default products...");
        switchMode(heData_trend, heData_MS, "he-theme", btnHe);
        return;
    }
    
    let gender = user.gender.toLowerCase();
    
    if (gender === "male") {
        console.log("Male user detected");
        switchMode(heData_trend, heData_MS, "he-theme", btnHe);
    } 
    else if (gender === "female") {
        console.log("Female user detected");
        switchMode(sheData_trend, sheData_MS, "she-theme", btnShe);
    } 
    else {
        console.log("Unknown gender. Showing default products.");
        switchMode(heData_trend, heData_MS, "he-theme", btnHe);
    }
}




function bubbleEffect(btn) {
    btn.classList.add("theme-bubble");
    setTimeout(() => btn.classList.remove("theme-bubble"), 400);
}


function switchMode(trendData, msData, theme, activeBtn) {
    document.body.className = "";
    document.body.classList.add(theme);

    render(trendData, Trending);
    render(msData, Most_selled);

    bubbleEffect(activeBtn);
}


btnHe.addEventListener("click", () => {
    bubbleEffect(btnHe);
    switchMode(heData_trend, heData_MS, "he-theme" , btnHe);
});

btnShe.addEventListener("click", () => {
    bubbleEffect(btnShe);

    switchMode(sheData_trend, sheData_MS, "she-theme" , btnShe);
});




